// FOR WHILE DO

void main() {
  for (int x = 0; x <= 10; x++) {
    print(x);
  }
  for (int y = 1; y <= 3; y++) {
    print("--------------------------------------");
  }
  int z = 12;
  while (z <= 20) {
    print(z);
    z++;
  }
  for (int y = 1; y <= 3; y++) {
    print("----------------------------------------");
  }
  int a = 0;
  do {
    print(a);
    (a++);
  } while (a <= 10);
}
